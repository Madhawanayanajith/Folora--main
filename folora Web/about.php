          <p>
          Folora is a mobile application belonging to the transportation category. The main purpose of this app is to easily find a reliable driver for passengers to reach their destination and to increase the income of those who work in the passenger transportation industry.
The project consists of two main parts. That is the admin section and the user section. Here, a mobile application is developed for the use of drivers and passengers. Looking at the drivers’ participation, this application can register drivers in four categories. That is, bike drivers, bus drivers, three-wheel drivers, car and van driver. Drivers can register easily by giving their information to this app through the register interface. Through the vehicle services interface of this app, the passenger can directly contact the nearest reliable vehicle. A website is being developed for this project for the administrators’ use. There is a separate section of roles that administrators contribute to. Admin can check how many vehicles are registered and the details of those drivers. If the admin receives a complaint about a driver from passengers, then the admin can make an inquiry and remove the registered drivers from this app.
